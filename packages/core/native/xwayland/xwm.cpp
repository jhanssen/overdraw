// Drain any pending GetProperty replies that xcb has already buffered. We poll
// each outstanding cookie; xcb_poll_for_reply does not block. A returned
// reply (or error) consumes that pending entry.
void drainPropertyReplies(XwmConn* x,
                          const std::function<void(const XwmEvent&)>& cb) {
    auto it = x->pending.begin();
    while (it != x->pending.end()) {
        void* reply = nullptr;
        xcb_generic_error_t* err = nullptr;
        const int ready = xcb_poll_for_reply(x->conn, it->sequence, &reply, &err);
        if (ready == 0) { ++it; continue; }  // not yet -- keep pending

        XwmEvent o;
        o.type = XwmEvent::PropertyReply;
        o.window = it->window;
        o.atom = it->atom;
        o.cookieId = it->cookieId;
        if (reply != nullptr) {
            auto* r = static_cast<xcb_get_property_reply_t*>(reply);
            o.replyType = r->type;
            o.format = r->format;
            o.length = static_cast<uint32_t>(xcb_get_property_value_length(r));
            o.data = static_cast<const uint8_t*>(xcb_get_property_value(r));
            cb(o);
            free(reply);
        } else {
            // Error or BadWindow: deliver an empty reply (format=0) so the
            // TS state machine can clear the pending entry.
            cb(o);
            if (err) free(err);
        }
        it = x->pending.erase(it);
    }
}

// Drain pending GetAtomName replies. Mirrors drainPropertyReplies; the
// reply carries a UTF-8 name borrowed from xcb's reply buffer.
void drainAtomNameReplies(XwmConn* x,
                          const std::function<void(const XwmEvent&)>& cb) {
    auto it = x->pendingAtomName.begin();
    while (it != x->pendingAtomName.end()) {
        void* reply = nullptr;
        xcb_generic_error_t* err = nullptr;
        const int ready = xcb_poll_for_reply(x->conn, it->sequence, &reply, &err);
        if (ready == 0) { ++it; continue; }

        XwmEvent o;
        o.type = XwmEvent::AtomNameReply;
        o.atom = it->atom;
        o.cookieId = it->cookieId;
        if (reply != nullptr) {
            auto* r = static_cast<xcb_get_atom_name_reply_t*>(reply);
            o.data = reinterpret_cast<const uint8_t*>(xcb_get_atom_name_name(r));
            o.length = static_cast<uint32_t>(xcb_get_atom_name_name_length(r));
            cb(o);
            free(reply);
        } else {
            // Error: deliver an empty name so the TS state machine clears
            // the pending entry.
            cb(o);
            if (err) free(err);
        }
        it = x->pendingAtomName.erase(it);
    }
}

