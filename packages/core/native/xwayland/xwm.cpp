const char* atomName(int i) {
    return (i >= 0 && i < ATOM_COUNT) ? ATOM_NAMES[i] : "";
}

// Tracked X-side geometry of a window. Updated on CreateNotify and
// ConfigureNotify; read on MapNotify so the TS side knows the rect Xwayland
// has on the window at map time (which may differ from CreateNotify's
// initial rect, e.g. for override-redirect windows that issue
// ConfigureWindow between create and map to position their menu).
struct TrackedRect {
    int32_t x = 0, y = 0, width = 0, height = 0;
};

struct XwmConn {
    xcb_connection_t* conn = nullptr;
    xcb_screen_t* screen = nullptr;
    xcb_window_t root = 0;
    xcb_atom_t atoms[ATOM_COUNT] = {0};

    // Pending GetProperty replies. Walked every xwmProcess call.
    std::vector<PendingProperty> pending;
    // Pending GetAtomName replies.
    std::vector<PendingAtomName> pendingAtomName;
    uint32_t nextCookieId = 1;

    // xfixes extension: the first-event opcode for selection-notify
    // (selection-bridge events arrive as (response_type & 0x7f) ==
    // xfixesFirstEvent + XCB_XFIXES_SELECTION_NOTIFY).
    uint8_t xfixesFirstEvent = 0;
    bool xfixesAvailable = false;

    // Per-window geometry tracker. Populated on CreateNotify, updated on
    // ConfigureNotify, cleared on DestroyNotify.
    std::unordered_map<uint32_t, TrackedRect> rects;

    // The bookkeeper window: 1x1 override-redirect, mapped offscreen. The
    // XWM SetInputFocuses this when no managed X client should hold X-side
    // focus (e.g. focus moved to a Wayland client or to a NULL focus
    // target). Created in xwmConnect; destroyed in xwmDisconnect.
    xcb_window_t bookkeeper = 0;

    // Default cursor set on the root window so X11 clients that don't
    // define their own inherit it. Created in xwmConnect, freed in
    // xwmDisconnect.
    xcb_cursor_t rootCursor = 0;

    // Sequence of the most recent SetInputFocus request the XWM issued. X
    // FocusIn events with .sequence older than this are stale -- they were
    // generated by a focus change WE initiated, OR by a client that lost
    // the race to our subsequent SetInputFocus. The TS side compares each
    // FocusIn event's sequence to this and discards stale ones.
    uint32_t lastFocusSeq = 0;
};

XwmConn* xwmConnect(int wmFd) {
    auto* x = new XwmConn();
    x->conn = xcb_connect_to_fd(wmFd, nullptr);
    if (xcb_connection_has_error(x->conn)) {
        LOG_ERR(Wayland, "[xwm] xcb_connect_to_fd failed");
        xcb_disconnect(x->conn);  // also closes wmFd
        delete x;
        return nullptr;
    }
    x->screen = xcb_setup_roots_iterator(xcb_get_setup(x->conn)).data;
    if (!x->screen) {
        LOG_ERR(Wayland, "[xwm] no screen");
        xcb_disconnect(x->conn);
        delete x;
        return nullptr;
    }
    x->root = x->screen->root;

    // Become the WM: redirect substructure (intercept map/configure requests)
    // and watch child lifecycle + root property changes.
    const uint32_t rootMask = XCB_EVENT_MASK_SUBSTRUCTURE_REDIRECT |
                              XCB_EVENT_MASK_SUBSTRUCTURE_NOTIFY |
                              XCB_EVENT_MASK_PROPERTY_CHANGE;
    xcb_change_window_attributes(x->conn, x->root, XCB_CW_EVENT_MASK, &rootMask);

    // Composite-redirect the root's subwindows (manual) so Xwayland presents
    // each toplevel as its own wl_surface instead of painting the root.
    xcb_composite_redirect_subwindows(x->conn, x->root, XCB_COMPOSITE_REDIRECT_MANUAL);

    // Root cursor: an X11 window that never calls XDefineCursor inherits its
    // cursor from an ancestor, ultimately the root. With no root cursor such
    // a window resolves to None, which Xwayland reports to the compositor as
    // wl_pointer.set_cursor(NULL) -- i.e. "hide the cursor". Set a standard
    // left-pointer glyph on the root so those windows show a cursor; a window
    // that explicitly hides its cursor still sends NULL and still hides.
    xcb_font_t cursorFont = xcb_generate_id(x->conn);
    xcb_open_font(x->conn, cursorFont, 6, "cursor");
    x->rootCursor = xcb_generate_id(x->conn);
    // XC_left_ptr = 68 in the X cursor font; its shape mask is the next glyph.
    xcb_create_glyph_cursor(x->conn, x->rootCursor, cursorFont, cursorFont,
                            68, 69, 0, 0, 0, 0xffff, 0xffff, 0xffff);
    xcb_change_window_attributes(x->conn, x->root, XCB_CW_CURSOR, &x->rootCursor);
    xcb_close_font(x->conn, cursorFont);

    // xfixes: required for selection-owner-change notifications. Query the
    // extension first-event opcode and bump the negotiated version (any
    // version that supports xfixes-selection is fine; we only need v1).
    const xcb_query_extension_reply_t* xfixesExt =
        xcb_get_extension_data(x->conn, &xcb_xfixes_id);
    if (xfixesExt && xfixesExt->present) {
        xcb_xfixes_query_version_cookie_t qvc = xcb_xfixes_query_version(
            x->conn, XCB_XFIXES_MAJOR_VERSION, XCB_XFIXES_MINOR_VERSION);
        xcb_xfixes_query_version_reply_t* qv =
            xcb_xfixes_query_version_reply(x->conn, qvc, nullptr);
        if (qv) {
            x->xfixesFirstEvent = xfixesExt->first_event;
            x->xfixesAvailable = true;
            free(qv);
        }
    }
    if (!x->xfixesAvailable) {
        LOG_WARN(Wayland,
            "[xwm] xfixes unavailable; clipboard / primary-selection bridge "
            "will not function");
    }

    // Intern atoms (pipelined: issue all, then collect).
    xcb_intern_atom_cookie_t cookies[ATOM_COUNT];
    for (int i = 0; i < ATOM_COUNT; ++i) {
        cookies[i] = xcb_intern_atom(
            x->conn, 0, static_cast<uint16_t>(std::strlen(ATOM_NAMES[i])), ATOM_NAMES[i]);
    }
    for (int i = 0; i < ATOM_COUNT; ++i) {
        xcb_intern_atom_reply_t* r = xcb_intern_atom_reply(x->conn, cookies[i], nullptr);
        if (r) {
            x->atoms[i] = r->atom;
            free(r);
        }
    }

    // Bookkeeper window: 1x1 override-redirect, parked offscreen. Selects
    // FOCUS_CHANGE so we can observe what X is doing with focus when the
    // bookkeeper holds it (mostly a noise filter; it doesn't drive any
    // policy). Mapped so SetInputFocus on it actually grants focus (X
    // refuses to focus an unmapped window).
    x->bookkeeper = xcb_generate_id(x->conn);
    const uint32_t bkValues[2] = { 1 /*override_redirect*/,
                                   XCB_EVENT_MASK_FOCUS_CHANGE };
    xcb_create_window(x->conn,
        XCB_COPY_FROM_PARENT, x->bookkeeper, x->root,
        -100, -100, 1, 1, 0,
        XCB_WINDOW_CLASS_COPY_FROM_PARENT,
        XCB_COPY_FROM_PARENT,
        XCB_CW_OVERRIDE_REDIRECT | XCB_CW_EVENT_MASK,
        bkValues);
    xcb_map_window(x->conn, x->bookkeeper);

    xcb_flush(x->conn);
    return x;
}

void xwmDisconnect(XwmConn* x) {
    if (!x) return;
    if (x->conn && x->bookkeeper) {
        xcb_destroy_window(x->conn, x->bookkeeper);
    }
    if (x->conn && x->rootCursor) {
        xcb_free_cursor(x->conn, x->rootCursor);
    }
    if (x->conn) xcb_disconnect(x->conn);
    delete x;
}

int xwmFd(XwmConn* x) { return xcb_get_file_descriptor(x->conn); }

uint32_t xwmAtom(XwmConn* x, int idx) {
    if (idx < 0 || idx >= ATOM_COUNT) return 0;
    return x->atoms[idx];
}

