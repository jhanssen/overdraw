bool xwmProcess(XwmConn* x, const std::function<void(const XwmEvent&)>& cb) {
    xcb_generic_event_t* ev;
    while ((ev = xcb_poll_for_event(x->conn))) {
        switch (ev->response_type & 0x7f) {
            case XCB_CREATE_NOTIFY: {
                auto* e = reinterpret_cast<xcb_create_notify_event_t*>(ev);
                // Don't surface internal WM-owned windows (the bookkeeper) to
                // the TS layer. It's our private focus-recipient; the TS XWM
                // would otherwise see it as an OR overlay and try to manage it.
                if (e->window == x->bookkeeper) break;
                // Select per-window events (focus + property changes); this also
                // routes the window's client-messages (incl. WL_SURFACE_SERIAL)
                // to us.
                const uint32_t mask =
                    XCB_EVENT_MASK_FOCUS_CHANGE | XCB_EVENT_MASK_PROPERTY_CHANGE;
                xcb_change_window_attributes(x->conn, e->window, XCB_CW_EVENT_MASK, &mask);
                // Track the initial geometry so MapNotify can report the
                // current rect even if no ConfigureNotify has fired yet.
                x->rects[e->window] = { e->x, e->y, e->width, e->height };
                XwmEvent o;
                o.type = XwmEvent::Create;
                o.window = e->window;
                o.x = e->x;
                o.y = e->y;
                o.width = e->width;
                o.height = e->height;
                o.overrideRedirect = e->override_redirect;
                cb(o);
                break;
            }
            case XCB_DESTROY_NOTIFY: {
                auto* e = reinterpret_cast<xcb_destroy_notify_event_t*>(ev);
                if (e->window == x->bookkeeper) break;
                x->rects.erase(e->window);
                XwmEvent o;
                o.type = XwmEvent::Destroy;
                o.window = e->window;
                cb(o);
                break;
            }
            case XCB_MAP_REQUEST: {
                auto* e = reinterpret_cast<xcb_map_request_event_t*>(ev);
                XwmEvent o;
                o.type = XwmEvent::MapRequest;
                o.window = e->window;
                cb(o);
                break;
            }
            case XCB_MAP_NOTIFY: {
                auto* e = reinterpret_cast<xcb_map_notify_event_t*>(ev);
                if (e->window == x->bookkeeper) break;
                XwmEvent o;
                o.type = XwmEvent::MapNotify;
                o.window = e->window;
                o.overrideRedirect = e->override_redirect;
                // Carry the current tracked rect so the TS side has the X-side
                // geometry at map time (CreateNotify's rect can be stale if
                // the client called ConfigureWindow between create and map).
                auto it = x->rects.find(e->window);
                if (it != x->rects.end()) {
                    o.x = it->second.x;
                    o.y = it->second.y;
                    o.width = it->second.width;
                    o.height = it->second.height;
                }
                cb(o);
                break;
            }
            case XCB_UNMAP_NOTIFY: {
                auto* e = reinterpret_cast<xcb_unmap_notify_event_t*>(ev);
                if (e->window == x->bookkeeper) break;
                XwmEvent o;
                o.type = XwmEvent::UnmapNotify;
                o.window = e->window;
                cb(o);
                break;
            }
            case XCB_CONFIGURE_REQUEST: {
                auto* e = reinterpret_cast<xcb_configure_request_event_t*>(ev);
                XwmEvent o;
                o.type = XwmEvent::ConfigureRequest;
                o.window = e->window;
                o.x = e->x;
                o.y = e->y;
                o.width = e->width;
                o.height = e->height;
                cb(o);
                break;
            }
            case XCB_CONFIGURE_NOTIFY: {
                auto* e = reinterpret_cast<xcb_configure_notify_event_t*>(ev);
                if (e->window == x->bookkeeper) break;
                // Track for future MapNotify reads. Skip our own synthetic
                // ConfigureNotifies (top bit of response_type set by
                // xcb_send_event) so we don't ping-pong on our own state.
                if ((ev->response_type & 0x80) == 0) {
                    x->rects[e->window] = { e->x, e->y, e->width, e->height };
                }
                XwmEvent o;
                o.type = XwmEvent::ConfigureNotify;
                o.window = e->window;
                o.x = e->x;
                o.y = e->y;
                o.width = e->width;
                o.height = e->height;
                cb(o);
                break;
            }
            case XCB_FOCUS_IN: {
                auto* e = reinterpret_cast<xcb_focus_in_event_t*>(ev);
                // Drop pointer-detail (cursor-motion focus chatter) and the
                // grab/ungrab modes (those are an X client temporarily
                // grabbing input, not a focus-policy event).
                if (e->detail == XCB_NOTIFY_DETAIL_POINTER) break;
                if (e->mode == XCB_NOTIFY_MODE_GRAB
                    || e->mode == XCB_NOTIFY_MODE_UNGRAB) break;
                // The bookkeeper's FocusIns are noise -- they fire when we
                // park X focus there; the TS side doesn't care.
                if (e->event == x->bookkeeper) break;
                XwmEvent o;
                o.type = XwmEvent::FocusIn;
                o.window = e->event;
                // X event sequence is 16-bit on the wire; widen to 32. TS
                // compares against the 32-bit sequence stashed in
                // lastFocusSeq, handling u16 wraparound with the same
                // half-window-rev-distance trick.
                o.eventSequence = e->sequence;
                cb(o);
                break;
            }
            case XCB_PROPERTY_NOTIFY: {
                auto* e = reinterpret_cast<xcb_property_notify_event_t*>(ev);
                XwmEvent o;
                o.type = XwmEvent::PropertyNotify;
                o.window = e->window;
                o.atom = e->atom;
                // XCB_PROPERTY_NEW_VALUE = 0, XCB_PROPERTY_DELETE = 1.
                // INCR pumps key off the distinction (incoming on NEW_VALUE,
                // outgoing on DELETE).
                o.propertyState = e->state;
                cb(o);
                break;
            }
            case XCB_SELECTION_REQUEST: {
                auto* e = reinterpret_cast<xcb_selection_request_event_t*>(ev);
                XwmEvent o;
                o.type = XwmEvent::SelectionRequest;
                o.requestor = e->requestor;
                o.selection = e->selection;
                o.target = e->target;
                o.property = e->property;  // 0 = obsolete-client convention
                o.timestamp = e->time;
                cb(o);
                break;
            }
            case XCB_SELECTION_NOTIFY: {
                auto* e = reinterpret_cast<xcb_selection_notify_event_t*>(ev);
                XwmEvent o;
                o.type = XwmEvent::SelectionNotify;
                o.requestor = e->requestor;
                o.selection = e->selection;
                o.target = e->target;
                o.property = e->property;  // 0 = conversion refused
                o.timestamp = e->time;
                cb(o);
                break;
            }
            case XCB_CLIENT_MESSAGE: {
                auto* e = reinterpret_cast<xcb_client_message_event_t*>(ev);
                if (e->type == x->atoms[ATOM_WL_SURFACE_SERIAL] && e->format == 32) {
                    const uint32_t lo = e->data.data32[0];
                    const uint32_t hi = e->data.data32[1];
                    XwmEvent o;
                    o.type = XwmEvent::SurfaceSerial;
                    o.window = e->window;
                    o.serial = (static_cast<uint64_t>(hi) << 32) | lo;
                    cb(o);
                } else if (e->type == x->atoms[ATOM_NET_WM_STATE] && e->format == 32) {
                    // EWMH §5.7: post-map state changes arrive as a root-window
                    // ClientMessage (action + up to two state atoms), not as a
                    // property write by the client.
                    XwmEvent o;
                    o.type = XwmEvent::NetWmState;
                    o.window = e->window;
                    o.stateAction = e->data.data32[0];
                    o.stateAtom1 = e->data.data32[1];
                    o.stateAtom2 = e->data.data32[2];
                    cb(o);
                }
                break;
            }
            default:
                if (x->xfixesAvailable
                    && (ev->response_type & 0x7f)
                        == x->xfixesFirstEvent + XCB_XFIXES_SELECTION_NOTIFY) {
                    auto* e = reinterpret_cast<
                        xcb_xfixes_selection_notify_event_t*>(ev);
                    XwmEvent o;
                    o.type = XwmEvent::XfixesSelectionNotify;
                    o.selection = e->selection;
                    o.selectionOwner = e->owner;  // XCB_NONE = no owner
                    o.timestamp = e->timestamp;
                    cb(o);
                }
                break;
        }
        free(ev);
    }

    drainPropertyReplies(x, cb);
    drainAtomNameReplies(x, cb);

    return !xcb_connection_has_error(x->conn);
}

void xwmMapWindow(XwmConn* x, uint32_t window) {
    xcb_map_window(x->conn, window);
    xcb_flush(x->conn);
}

void xwmConfigureWindow(XwmConn* x, uint32_t window,
                        int32_t xx, int32_t yy, int32_t w, int32_t h) {
    const uint32_t values[] = {
        static_cast<uint32_t>(xx), static_cast<uint32_t>(yy),
        static_cast<uint32_t>(w), static_cast<uint32_t>(h),
    };
    const uint16_t mask = XCB_CONFIG_WINDOW_X | XCB_CONFIG_WINDOW_Y |
                          XCB_CONFIG_WINDOW_WIDTH | XCB_CONFIG_WINDOW_HEIGHT;
    xcb_configure_window(x->conn, window, mask, values);
    xcb_flush(x->conn);
}

void xwmSendConfigureNotify(XwmConn* x, uint32_t window,
                            int32_t xx, int32_t yy, int32_t w, int32_t h) {
    xcb_configure_notify_event_t ev = {};
    ev.response_type = XCB_CONFIGURE_NOTIFY;
    ev.event = window;
    ev.window = window;
    ev.above_sibling = XCB_NONE;
    ev.x = static_cast<int16_t>(xx);
    ev.y = static_cast<int16_t>(yy);
    ev.width = static_cast<uint16_t>(w);
    ev.height = static_cast<uint16_t>(h);
    ev.border_width = 0;
    ev.override_redirect = 0;
    xcb_send_event(x->conn, /*propagate=*/0, window,
                   XCB_EVENT_MASK_STRUCTURE_NOTIFY,
                   reinterpret_cast<const char*>(&ev));
    xcb_flush(x->conn);
}

uint32_t xwmGetProperty(XwmConn* x, uint32_t window, uint32_t atom,
                        uint32_t maxLengthWords) {
    // delete=0, type=AnyPropertyType(0), offset=0, long_length=maxLengthWords.
    xcb_get_property_cookie_t c = xcb_get_property(
        x->conn, /*_delete=*/0, window, atom, /*type=*/0,
        /*long_offset=*/0, maxLengthWords);
    xcb_flush(x->conn);
    PendingProperty p;
    p.cookieId = x->nextCookieId++;
    if (p.cookieId == 0) p.cookieId = x->nextCookieId++;  // skip 0 sentinel
    p.window = window;
    p.atom = atom;
    p.sequence = c.sequence;
    x->pending.push_back(p);
    return p.cookieId;
}

void xwmSendWmProtocol(XwmConn* x, uint32_t window, uint32_t proto,
                       uint32_t timestamp) {
    xcb_client_message_event_t ev = {};
    ev.response_type = XCB_CLIENT_MESSAGE;
    ev.format = 32;
    ev.window = window;
    ev.type = x->atoms[ATOM_WM_PROTOCOLS];
    ev.data.data32[0] = proto;
    ev.data.data32[1] = timestamp;
    ev.data.data32[2] = 0;
    ev.data.data32[3] = 0;
    ev.data.data32[4] = 0;
    xcb_send_event(x->conn, /*propagate=*/0, window, XCB_EVENT_MASK_NO_EVENT,
                   reinterpret_cast<const char*>(&ev));
    xcb_flush(x->conn);
}

