export interface XwmEventMsg {
  type:
    | "create"
    | "destroy"
    | "map-request"
    | "map"
    | "unmap"
    | "configure-request"
    | "configure-notify"
    | "surface-serial"
    | "property-notify"
    | "property-reply"
    | "focus-in"
    | "xfixes-selection-notify"
    | "selection-request"
    | "selection-notify"
    | "atom-name-reply";
  window: number;
  x: number;
  y: number;
  width: number;
  height: number;
  overrideRedirect: boolean;
  serialLo: number;
  serialHi: number;
  atom?: number;
  cookieId?: number;
  replyType?: number;
  format?: number;
  data?: Uint8Array;
  // focus-in only: the X event's sequence (16-bit, widened to 32 on the
  // native side). TS compares against lastFocusSeq to drop FocusIns
  // generated by WM-initiated SetInputFocus calls.
  eventSequence?: number;
  // property-notify only: 0 = NewValue, 1 = Delete. The selection bridge
  // keys both INCR pumps off this distinction.
  propertyState?: number;
  // Selection-bridge fields.
  selection?: number;
  target?: number;
  property?: number;
  requestor?: number;
  selectionOwner?: number;
  timestamp?: number;
  // atom-name-reply only.
  name?: string;
}

export interface XWindow {
  window: number;             // X11 window id
  x: number;
  y: number;
  width: number;
  height: number;
  overrideRedirect: boolean;
  mapped: boolean;
  surfaceId: number | null;   // resolved via WL_SURFACE_SERIAL
  addedToWm: boolean;
  // Parsed ICCCM / EWMH properties. null until the corresponding property
  // reply has landed (or the property is absent).
  title: string | null;
  appId: string | null;
  wmProtocols: Set<number>;   // atoms (incl. WM_DELETE_WINDOW when supported)
  transientFor: number | null; // X window id of parent toplevel
  minSize: { width: number; height: number } | null;
  maxSize: { width: number; height: number } | null;
  windowKind: ReturnType<typeof classifyWindowType>;
  presentationHint: "fullscreen" | "maximized" | null;
  // _NET_WM_STATE_MODAL: the client wishes the window to be modal
  // relative to its WM_TRANSIENT_FOR. Mapped onto
  // windowState.clientRequests.wantsModal so the policy seam decides.
  modalHint: boolean;
  // WM_HINTS.input: null when the InputHint bit isn't set (ICCCM default
  // means treat as true). Drives the SetInputFocus vs WM_TAKE_FOCUS
  // decision per the ICCCM truth table.
  inputHint: boolean | null;
  // _NET_WM_PID: the X client's process id, used for the same-PID
  // exception in cross-app focus stealing (a single multi-window app
  // re-routing focus among its own windows is allowed; cross-PID
  // attempts are denied by refocusing the previous surface).
  pid: number | null;
  // The full _NET_WM_STATE atom set as last observed. Cached so the
  // focus mirror can do a read-modify-write on _NET_WM_STATE_FOCUSED
  // without clobbering client-set bits (fullscreen / maximized /
  // modal / etc.). Refreshed on every _NET_WM_STATE property reply.
  netWmStateAtoms: Set<number>;
  // _NET_STARTUP_ID: an opaque ASCII id the launcher set on the window
  // (matched against the SI message the launcher emitted via dbus).
  // Exposed read-only via the XwmStateView so launchers / plugins can
  // correlate an X window to the activation that spawned it.
  startupId: string | null;
  // _NET_WM_ICON: ARGB icon image(s) set by the client. The full
  // property is a concatenation of [width:u32][height:u32][argb_data
  // ...] tuples; we keep the parsed list so plugins can pick the size
  // they want. null until the property arrives (or absent).
  icons: NetWmIcon[] | null;
  // Count of identity-bearing property reads (WM_CLASS + the title atoms)
  // still outstanding from the associate-time batch. maybeManage holds the
  // window out of the WM while this is > 0 so the window.preconfigure event
  // (and thus any window-rules matching) sees the real app_id/title before
  // the window maps, not a null placeholder. Decremented as each reply lands;
  // when it hits 0 the deferred manage runs. GetProperty always replies (a
  // missing property yields type=None), so this is bounded with no timer.
  criticalReadsPending: number;
}

// Property atoms that carry a window's identity (app_id / title). maybeManage
// waits for these before admitting the window so window-rules can match
// pre-map. The others in the watched batch (size hints, transient-for, state)
// continue to flow asynchronously and refine the window after it maps.
const CRITICAL_ATOM_NAMES: ReadonlySet<string> = new Set([
  "WM_CLASS", "_NET_WM_NAME", "WM_NAME",
]);

// One decoded _NET_WM_ICON entry. ARGB premultiplied, top-to-bottom,
// left-to-right; pixels.length === width * height (32 bits each).
export interface NetWmIcon {
  width: number;
  height: number;
  pixels: Uint32Array;
}

// A narrow view stashed on CompositorState so unrelated modules (titleAppId,
// close-surface) can look up X-backed windows or initiate their close path
// without taking a dep on the full Xwm.
export interface XwmStateView {
  findBySurfaceId(surfaceId: number): XWindow | null;
  // Initiate close. Returns true iff a matching X window was found; the
  // close-surface helper uses that to skip the xdg branch.
  closeBySurfaceId(surfaceId: number): boolean;
  // Re-narrate managed windows' glass positions after a camera or
  // stack-visibility change (glass-map.ts). Cheap when nothing moved.
  retellPositions(): void;
  // _NET_STARTUP_ID set on the X window (or null when the client never set
  // one). Resolved by surfaceId so callers don't need an X window handle.
  startupIdOf(surfaceId: number): string | null;
  // _NET_WM_ICON entries set on the X window (or null when the client
  // never set the property). Each entry is one ARGB icon at a particular
  // size; pick the closest to your render target.
  iconsOf(surfaceId: number): NetWmIcon[] | null;
}

export interface Xwm {
  stop(): void;
  windows(): ReadonlyMap<number, XWindow>;  // inspection (tests)
  // Initiate a close on an x-backed surface, by surfaceId. Sends a
  // WM_DELETE_WINDOW client-message when the client supports it, else
  // KillClient. Returns true iff a matching X window was found.
  closeBySurfaceId(surfaceId: number): boolean;
  // The selection bridge registers itself here so it can receive
  // selection-bridge events and claim property-notify deliveries on its own
  // per-transfer windows. `owner` returns true when the window id belongs to
  // a bridge-owned transfer; `hook` receives the four bridge event types
  // plus property-notify on bridge-owned windows.
  setSelectionHook(
    hook: ((ev: XwmEventMsg) => void) | null,
    owner: ((window: number) => boolean) | null,
  ): void;
  // The interned atom map (read-only). The selection bridge needs the
  // selection atoms (CLIPBOARD / PRIMARY / TARGETS / ...) to drive xcb.
  atoms(): Readonly<Record<string, number>>;
  // The X focus state visible to the WM, used by the bridge for focus
  // gating (X selection ownership claims only take effect when an X
  // client has X-side focus).
  xFocusedWindow(): number | null;
}

// All properties we batch-read on associate. PropertyNotify on any of these
// also re-reads only the one that changed.
function pickWatchedAtoms(atoms: Record<string, number>): Array<{ name: string; atom: number }> {
  const names = [
    "_NET_WM_NAME", "WM_NAME",
    "WM_CLASS",
    "WM_PROTOCOLS",
    "WM_NORMAL_HINTS",
    "WM_HINTS",
    "WM_TRANSIENT_FOR",
    "_NET_WM_STATE",
    "_NET_WM_WINDOW_TYPE",
    "_NET_WM_PID",
    "_NET_STARTUP_ID",
    "_NET_WM_ICON",
  ];
  return names.map((n) => ({ name: n, atom: atoms[n] ?? 0 })).filter((e) => e.atom !== 0);
}

